QXZBServerSelect.areaChange = function () {
    QXZBServerSelect.serverSelect.options.length = 0;

    var selectIndex = QXZBServerSelect.areaSelect.selectedIndex;
    for (var i = 0; i < QXZBServerSelect.STD_DATA[selectIndex].opt_data_array.length; i++) {
        var varItem = new Option(QXZBServerSelect.STD_DATA[selectIndex].opt_data_array[i].t, QXZBServerSelect.STD_DATA[selectIndex].opt_data_array[i].v);
        QXZBServerSelect.serverSelect.options.add(varItem);
    }

}


QXZBServerSelect.showzone = function (select_area, select_server) {
    QXZBServerSelect.areaSelect = select_area;
    QXZBServerSelect.serverSelect = select_server;

    for (var i = 0; i < QXZBServerSelect.STD_DATA.length; i++) {
        var varItem = new Option(QXZBServerSelect.STD_DATA[i].t, QXZBServerSelect.STD_DATA[i].v);
        QXZBServerSelect.areaSelect.options.add(varItem);
    }
    QXZBServerSelect.areaSelect.options[0].selected = true;
    QXZBServerSelect.areaSelect.attachEvent('onchange', QXZBServerSelect.areaChange);

    QXZBServerSelect.areaChange();

}


QXZBServerSelect.setZone =  function (area_id, server_id) {
    for(var i = 0; i<QXZBServerSelect.areaSelect.length; i++){
        if(area_id == QXZBServerSelect.areaSelect.options[i].value){
             QXZBServerSelect.areaSelect.options[i].selected = true;
             break;
        }
    }
    
    QXZBServerSelect.areaChange();
    
    for(var i = 0; i<QXZBServerSelect.serverSelect.length; i++){
        if(server_id == QXZBServerSelect.serverSelect.options[i].value){
             QXZBServerSelect.serverSelect.options[i].selected = true;
             break;
        }
    }    
    
}

